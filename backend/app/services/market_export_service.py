# Plain text generation
# ---------------------------------------------------------------------------

def generate_text(analysis: MarketAnalysis, project_name: str) -> str:
    """Generate a plain text market analysis report."""
    lines = []
    sep = "=" * 60

    lines.append(sep)
    lines.append(f"  {project_name}")
    lines.append("  Market Analysis Report")
    lines.append(f"  Generated: {datetime.utcnow().strftime('%B %d, %Y')}")
    lines.append(sep)
    lines.append("")

    # --- Executive Summary ---
    lines.append("EXECUTIVE SUMMARY")
    lines.append("-" * 40)
    summaries = []
    if analysis.target_market and isinstance(analysis.target_market, dict):
        summaries.append(analysis.target_market.get("summary", ""))
    if analysis.competitive_landscape and isinstance(analysis.competitive_landscape, dict):
        summaries.append(analysis.competitive_landscape.get("summary", ""))
    if analysis.marketing_strategies and isinstance(analysis.marketing_strategies, dict):
        summaries.append(analysis.marketing_strategies.get("summary", ""))
    lines.append(" ".join(filter(None, summaries)) or "No executive summary data available.")
    lines.append("")

    # Key metrics
    if analysis.market_metrics and isinstance(analysis.market_metrics, dict):
        metrics = analysis.market_metrics
        tam = _fmt_usd(metrics.get("tam", {}).get("value_usd", 0))
        sam = _fmt_usd(metrics.get("sam", {}).get("value_usd", 0))
        som = _fmt_usd(metrics.get("som", {}).get("value_usd", 0))
        lines.append(f"  TAM: {tam}  |  SAM: {sam}  |  SOM: {som}")
        lines.append(f"  Growth Rate: {metrics.get('growth_rate_pct', 'N/A')}%  |  Maturity: {_safe(metrics.get('market_maturity'))}")
        lines.append("")

    # --- Target Market ---
    if analysis.target_market and isinstance(analysis.target_market, dict):
        tm = analysis.target_market
        lines.append(sep)
        lines.append("TARGET MARKET ASSESSMENT")
        lines.append("-" * 40)
        lines.append(_safe(tm.get("summary")))
        lines.append("")

        demographics = tm.get("demographics", {})
        if demographics:
            lines.append("Demographics:")
            for key, val in demographics.items():
                lines.append(f"  {key.replace('_', ' ').title()}: {val}")
            lines.append("")

        segments = tm.get("market_segments", [])
        if segments:
            lines.append("Market Segments:")
            for seg in segments:
                lines.append(f"  * {seg.get('name', '')} ({seg.get('size_pct', 0)}%, {seg.get('priority', '')})")
                lines.append(f"    {seg.get('description', '')}")
            lines.append("")

        personas = tm.get("user_personas", [])
        if personas:
            lines.append("User Personas:")
            for persona in personas:
                lines.append(f"  * {persona.get('name', '')} -- {persona.get('role', '')}")
                lines.append(f"    Goal: {persona.get('goal', 'N/A')}")
                lines.append(f"    Frustration: {persona.get('frustration', 'N/A')}")
            lines.append("")

    # --- Competitive Landscape ---
    if analysis.competitive_landscape and isinstance(analysis.competitive_landscape, dict):
        cl = analysis.competitive_landscape
        lines.append(sep)
        lines.append("COMPETITIVE LANDSCAPE")
        lines.append("-" * 40)
        lines.append(_safe(cl.get("summary")))
        lines.append("")

        direct = cl.get("direct_competitors", [])
        if direct:
            lines.append("Direct Competitors:")
            for comp in direct:
                lines.append(f"  * {comp.get('name', '')} (Threat: {comp.get('threat_level', 'N/A')}, Share: {comp.get('market_share_pct', 'N/A')}%)")
                lines.append(f"    Strengths: {', '.join(comp.get('strengths', []))}")
                lines.append(f"    Weaknesses: {', '.join(comp.get('weaknesses', []))}")
                if comp.get("pricing"):
                    lines.append(f"    Pricing: {comp['pricing']}")
            lines.append("")

        advantages = cl.get("competitive_advantages", [])
        if advantages:
            lines.append("Your Competitive Advantages:")
            for adv in advantages:
                lines.append(f"  + {adv}")
            lines.append("")

        gaps = cl.get("market_gaps", [])
        if gaps:
            lines.append("Market Gaps:")
            for gap in gaps:
                lines.append(f"  * {gap}")
            lines.append("")

    # --- Revenue Projections ---
    if analysis.revenue_projections and isinstance(analysis.revenue_projections, dict):
        rev = analysis.revenue_projections
        lines.append(sep)
        lines.append("REVENUE PROJECTIONS")
        lines.append("-" * 40)
        lines.append(f"Pricing Model: {_safe(rev.get('pricing_model'))}")
        if rev.get("break_even_months"):
            lines.append(f"Break-Even: {rev['break_even_months']} months")
        lines.append("")

        tiers = rev.get("pricing_tiers", [])
        if tiers:
            lines.append("Pricing Tiers:")
            for tier in tiers:
                price = "Free" if tier.get("price_monthly", 0) == 0 else f"${tier.get('price_monthly', 0)}/mo"
                lines.append(f"  * {tier.get('name', '')} ({price}) -- {tier.get('target_segment', '')}")
            lines.append("")

        scenarios = rev.get("scenarios", {})
        if scenarios:
            lines.append("3-Year Revenue Scenarios:")
            for scenario_key, label in [("best_case", "Best Case"), ("likely_case", "Likely Case"), ("worst_case", "Worst Case")]:
                sc = scenarios.get(scenario_key, {})
                if sc:
                    lines.append(f"  {label}:")
                    for yr_key, yr_label in [("year_1", "Year 1"), ("year_2", "Year 2"), ("year_3", "Year 3")]:
                        yr = sc.get(yr_key, {})
                        lines.append(f"    {yr_label}: Revenue {_fmt_usd(yr.get('revenue', 0))}, MRR {_fmt_usd(yr.get('mrr', 0))}, Users {_fmt_number(yr.get('users', 0))}")
            lines.append("")

    # --- Marketing Strategy ---
    if analysis.marketing_strategies and isinstance(analysis.marketing_strategies, dict):
        ms = analysis.marketing_strategies
        lines.append(sep)
        lines.append("MARKETING STRATEGY")
        lines.append("-" * 40)
        lines.append(_safe(ms.get("summary")))
        lines.append("")

        budget = ms.get("budget_summary", {})
        if budget:
            lines.append(f"Monthly Budget: {_fmt_usd(budget.get('monthly_min_usd', 0))} -- {_fmt_usd(budget.get('monthly_max_usd', 0))}")
            lines.append("")

        channels = ms.get("channels", [])
        if channels:
            lines.append("Channels:")
            for ch in channels:
                lines.append(f"  * {ch.get('name', '')} ({ch.get('priority', '')}) -- Budget: {_fmt_usd(ch.get('budget_monthly_usd', 0))}/mo, CAC: ${ch.get('expected_cac', 0)}")
                tactics = ch.get("tactics", [])
                if tactics:
                    lines.append(f"    Tactics: {', '.join(tactics)}")
            lines.append("")

    lines.append(sep)
    lines.append("Generated by Ide/AI")
    lines.append(sep)

    return "\n".join(lines)
