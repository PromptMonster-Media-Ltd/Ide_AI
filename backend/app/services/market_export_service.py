def generate_pdf(analysis: MarketAnalysis, project_name: str) -> bytes:
    """Generate a professional market analysis PDF using fpdf2."""
    from fpdf import FPDF

    s = _sanitize_latin1

    pdf = FPDF()
    pdf.set_auto_page_break(auto=True, margin=20)

    # --- Title Page ---
    pdf.add_page()
    pdf.ln(50)
    pdf.set_font("Helvetica", "B", 28)
    pdf.cell(0, 15, s(project_name), new_x="LMARGIN", new_y="NEXT", align="C")
    pdf.ln(5)
    pdf.set_font("Helvetica", "", 18)
    pdf.cell(0, 12, "Market Analysis Report", new_x="LMARGIN", new_y="NEXT", align="C")
    pdf.ln(10)
    pdf.set_font("Helvetica", "", 11)
    pdf.cell(0, 8, f"Generated: {datetime.utcnow().strftime('%B %d, %Y')}", new_x="LMARGIN", new_y="NEXT", align="C")
    pdf.ln(60)
    pdf.set_font("Helvetica", "I", 9)
    pdf.cell(0, 8, "Generated by Ide/AI", new_x="LMARGIN", new_y="NEXT", align="C")

    # Helper: section header
    def section_header(title: str):
        pdf.add_page()
        pdf.set_font("Helvetica", "B", 18)
        pdf.cell(0, 12, s(title), new_x="LMARGIN", new_y="NEXT")
        pdf.ln(3)
        # Divider line
        pdf.set_draw_color(0, 229, 255)
        pdf.set_line_width(0.5)
        pdf.line(10, pdf.get_y(), 200, pdf.get_y())
        pdf.ln(6)

    def sub_header(title: str):
        pdf.set_font("Helvetica", "B", 13)
        pdf.cell(0, 10, s(title), new_x="LMARGIN", new_y="NEXT")
        pdf.ln(2)

    def body_text(text: str):
        pdf.set_font("Helvetica", "", 10)
        pdf.multi_cell(0, 5.5, s(text))
        pdf.ln(3)

    def bullet(text: str):
        pdf.set_font("Helvetica", "", 10)
        pdf.cell(5)
        pdf.multi_cell(0, 5.5, s(f"  * {text}"))

    # --- Executive Summary ---
    section_header("Executive Summary")
    summaries = []
    if analysis.target_market and isinstance(analysis.target_market, dict):
        summaries.append(analysis.target_market.get("summary", ""))
    if analysis.competitive_landscape and isinstance(analysis.competitive_landscape, dict):
        summaries.append(analysis.competitive_landscape.get("summary", ""))
    if analysis.marketing_strategies and isinstance(analysis.marketing_strategies, dict):
        summaries.append(analysis.marketing_strategies.get("summary", ""))
    body_text(" ".join(filter(None, summaries)) or "No executive summary data available.")

    # Key metrics snapshot
    if analysis.market_metrics and isinstance(analysis.market_metrics, dict):
        metrics = analysis.market_metrics
        sub_header("Key Market Metrics")
        pdf.set_font("Helvetica", "B", 11)
        tam_val = _fmt_usd(metrics.get("tam", {}).get("value_usd", 0))
        sam_val = _fmt_usd(metrics.get("sam", {}).get("value_usd", 0))
        som_val = _fmt_usd(metrics.get("som", {}).get("value_usd", 0))
        pdf.cell(60, 8, s(f"TAM: {tam_val}"), border=1, align="C")
        pdf.cell(60, 8, s(f"SAM: {sam_val}"), border=1, align="C")
        pdf.cell(60, 8, s(f"SOM: {som_val}"), border=1, new_x="LMARGIN", new_y="NEXT", align="C")
        pdf.ln(3)
        growth = metrics.get("growth_rate_pct", "N/A")
        maturity = _safe(metrics.get("market_maturity"))
        pdf.set_font("Helvetica", "", 10)
        pdf.cell(0, 6, s(f"Growth Rate: {growth}%  |  Market Maturity: {maturity}"), new_x="LMARGIN", new_y="NEXT")
        pdf.ln(5)

    # --- Target Market ---
    if analysis.target_market and isinstance(analysis.target_market, dict):
        tm = analysis.target_market
        section_header("Target Market Assessment")
        body_text(_safe(tm.get("summary")))

        # Demographics table
        demographics = tm.get("demographics", {})
        if demographics:
            sub_header("Demographics")
            pdf.set_font("Helvetica", "B", 10)
            pdf.cell(60, 7, "Attribute", border=1)
            pdf.cell(0, 7, "Value", border=1, new_x="LMARGIN", new_y="NEXT")
            pdf.set_font("Helvetica", "", 10)
            for key, val in demographics.items():
                label = key.replace("_", " ").title()
                pdf.cell(60, 7, s(label), border=1)
                pdf.cell(0, 7, s(str(val)), border=1, new_x="LMARGIN", new_y="NEXT")
            pdf.ln(5)

        # Market segments
        segments = tm.get("market_segments", [])
        if segments:
            sub_header("Market Segments")
            pdf.set_font("Helvetica", "B", 10)
            pdf.cell(50, 7, "Segment", border=1)
            pdf.cell(25, 7, "Size %", border=1, align="C")
            pdf.cell(25, 7, "Priority", border=1, align="C")
            pdf.cell(0, 7, "Description", border=1, new_x="LMARGIN", new_y="NEXT")
            pdf.set_font("Helvetica", "", 9)
            for seg in segments:
                pdf.cell(50, 7, s(seg.get("name", "")), border=1)
                pdf.cell(25, 7, s(f"{seg.get('size_pct', 0)}%"), border=1, align="C")
                pdf.cell(25, 7, s(seg.get("priority", "")), border=1, align="C")
                desc = seg.get("description", "")[:60]
                pdf.cell(0, 7, s(desc), border=1, new_x="LMARGIN", new_y="NEXT")
            pdf.ln(5)

        # User personas
        personas = tm.get("user_personas", [])
        if personas:
            sub_header("User Personas")
            for persona in personas:
                pdf.set_font("Helvetica", "B", 10)
                pdf.cell(0, 7, s(f"{persona.get('name', 'Unnamed')} -- {persona.get('role', '')}"), new_x="LMARGIN", new_y="NEXT")
                pdf.set_font("Helvetica", "", 10)
                pdf.cell(0, 6, s(f"  Goal: {persona.get('goal', 'N/A')}"), new_x="LMARGIN", new_y="NEXT")
                pdf.cell(0, 6, s(f"  Frustration: {persona.get('frustration', 'N/A')}"), new_x="LMARGIN", new_y="NEXT")
                pdf.ln(2)

    # --- Competitive Landscape ---
    if analysis.competitive_landscape and isinstance(analysis.competitive_landscape, dict):
        cl = analysis.competitive_landscape
        section_header("Competitive Landscape")
        body_text(_safe(cl.get("summary")))

        # Competitors table
        direct = cl.get("direct_competitors", [])
        if direct:
            sub_header("Direct Competitors")
            pdf.set_font("Helvetica", "B", 9)
            pdf.cell(35, 7, "Competitor", border=1)
            pdf.cell(20, 7, "Share", border=1, align="C")
            pdf.cell(20, 7, "Threat", border=1, align="C")
            pdf.cell(45, 7, "Strengths", border=1)
            pdf.cell(0, 7, "Weaknesses", border=1, new_x="LMARGIN", new_y="NEXT")
            pdf.set_font("Helvetica", "", 8)
            for comp in direct:
                name = comp.get("name", "")[:18]
                share = f"{comp.get('market_share_pct', 'N/A')}%"
                threat = comp.get("threat_level", "N/A")
                strengths = ", ".join(comp.get("strengths", []))[:40]
                weaknesses = ", ".join(comp.get("weaknesses", []))[:40]
                pdf.cell(35, 7, s(name), border=1)
                pdf.cell(20, 7, s(share), border=1, align="C")
                pdf.cell(20, 7, s(threat), border=1, align="C")
                pdf.cell(45, 7, s(strengths), border=1)
                pdf.cell(0, 7, s(weaknesses), border=1, new_x="LMARGIN", new_y="NEXT")
            pdf.ln(5)

        # Competitive advantages
        advantages = cl.get("competitive_advantages", [])
        if advantages:
            sub_header("Your Competitive Advantages")
            for adv in advantages:
                bullet(adv)
            pdf.ln(3)

        # Market gaps
        gaps = cl.get("market_gaps", [])
        if gaps:
            sub_header("Market Gaps & Opportunities")
            for gap in gaps:
                bullet(gap)
            pdf.ln(3)

    # --- Market Metrics ---
    if analysis.market_metrics and isinstance(analysis.market_metrics, dict):
        metrics = analysis.market_metrics
        section_header("Market Metrics & TAM/SAM/SOM")

        # TAM/SAM/SOM detailed
        for key_name, label in [("tam", "TAM (Total Addressable Market)"), ("sam", "SAM (Serviceable Addressable Market)"), ("som", "SOM (Serviceable Obtainable Market)")]:
            metric = metrics.get(key_name, {})
            if metric:
                sub_header(label)
                pdf.set_font("Helvetica", "B", 14)
                pdf.cell(0, 10, s(_fmt_usd(metric.get("value_usd", 0))), new_x="LMARGIN", new_y="NEXT")
                pdf.set_font("Helvetica", "", 10)
                body_text(_safe(metric.get("description")))
                if metric.get("methodology"):
                    pdf.set_font("Helvetica", "I", 9)
                    pdf.multi_cell(0, 5, s(f"Methodology: {metric['methodology']}"))
                    pdf.ln(3)

        # Industry trends
        trends = metrics.get("industry_trends", [])
        if trends:
            sub_header("Industry Trends")
            for trend in trends:
                direction = trend.get("direction", "neutral")
                indicator = "+" if direction == "positive" else "-" if direction == "negative" else "~"
                bullet(f"[{indicator}] {trend.get('trend', '')} -- {trend.get('impact', '')}")
            pdf.ln(3)

    # --- Revenue Projections ---
    if analysis.revenue_projections and isinstance(analysis.revenue_projections, dict):
        rev = analysis.revenue_projections
        section_header("Revenue Projections")

        # Pricing model
        pdf.set_font("Helvetica", "B", 11)
        pdf.cell(0, 8, s(f"Recommended Model: {_safe(rev.get('pricing_model'))}"), new_x="LMARGIN", new_y="NEXT")
        if rev.get("break_even_months"):
            pdf.set_font("Helvetica", "", 10)
            pdf.cell(0, 6, s(f"Estimated Break-Even: {rev['break_even_months']} months"), new_x="LMARGIN", new_y="NEXT")
        pdf.ln(5)

        # Pricing tiers table
        tiers = rev.get("pricing_tiers", [])
        if tiers:
            sub_header("Pricing Tiers")
            pdf.set_font("Helvetica", "B", 9)
            pdf.cell(35, 7, "Tier", border=1)
            pdf.cell(25, 7, "Price/mo", border=1, align="C")
            pdf.cell(40, 7, "Target Segment", border=1)
            pdf.cell(0, 7, "Key Features", border=1, new_x="LMARGIN", new_y="NEXT")
            pdf.set_font("Helvetica", "", 9)
            for tier in tiers:
                price = "Free" if tier.get("price_monthly", 0) == 0 else f"${tier.get('price_monthly', 0)}/mo"
                features = ", ".join(tier.get("features", []))[:50]
                pdf.cell(35, 7, s(tier.get("name", "")), border=1)
                pdf.cell(25, 7, s(price), border=1, align="C")
                pdf.cell(40, 7, s(tier.get("target_segment", "")[:25]), border=1)
                pdf.cell(0, 7, s(features), border=1, new_x="LMARGIN", new_y="NEXT")
            pdf.ln(5)

        # 3-Year projections table
        scenarios = rev.get("scenarios", {})
        if scenarios:
            sub_header("3-Year Revenue Scenarios")
            pdf.set_font("Helvetica", "B", 9)
            pdf.cell(25, 7, "Scenario", border=1)
            pdf.cell(25, 7, "Year 1", border=1, align="C")
            pdf.cell(25, 7, "Year 2", border=1, align="C")
            pdf.cell(25, 7, "Year 3", border=1, align="C")
            pdf.cell(25, 7, "Yr1 MRR", border=1, align="C")
            pdf.cell(25, 7, "Yr3 Users", border=1, align="C")
            pdf.cell(0, 7, "Yr3 Paying", border=1, new_x="LMARGIN", new_y="NEXT", align="C")
            pdf.set_font("Helvetica", "", 9)
            for scenario_key, label in [("best_case", "Best"), ("likely_case", "Likely"), ("worst_case", "Worst")]:
                sc = scenarios.get(scenario_key, {})
                y1 = sc.get("year_1", {})
                y2 = sc.get("year_2", {})
                y3 = sc.get("year_3", {})
                pdf.cell(25, 7, s(label), border=1)
                pdf.cell(25, 7, s(_fmt_usd(y1.get("revenue", 0))), border=1, align="C")
                pdf.cell(25, 7, s(_fmt_usd(y2.get("revenue", 0))), border=1, align="C")
                pdf.cell(25, 7, s(_fmt_usd(y3.get("revenue", 0))), border=1, align="C")
                pdf.cell(25, 7, s(_fmt_usd(y1.get("mrr", 0))), border=1, align="C")
                pdf.cell(25, 7, s(_fmt_number(y3.get("users", 0))), border=1, align="C")
                pdf.cell(0, 7, s(_fmt_number(y3.get("paying_users", 0))), border=1, new_x="LMARGIN", new_y="NEXT", align="C")
            pdf.ln(5)

    # --- Marketing Strategy ---
    if analysis.marketing_strategies and isinstance(analysis.marketing_strategies, dict):
        ms = analysis.marketing_strategies
        section_header("Marketing Strategy")
        body_text(_safe(ms.get("summary")))

        # Budget summary
        budget = ms.get("budget_summary", {})
        if budget:
            sub_header("Budget Overview")
            pdf.set_font("Helvetica", "", 10)
            monthly_min = _fmt_usd(budget.get("monthly_min_usd", 0))
            monthly_max = _fmt_usd(budget.get("monthly_max_usd", 0))
            pdf.cell(0, 7, s(f"Monthly Range: {monthly_min} -- {monthly_max}"), new_x="LMARGIN", new_y="NEXT")
            allocation = budget.get("allocation", [])
            if allocation:
                for alloc in allocation:
                    bullet(f"{alloc.get('category', '')}: {alloc.get('pct', 0)}%")
            pdf.ln(3)

        # Channels
        channels = ms.get("channels", [])
        if channels:
            sub_header("Marketing Channels")
            pdf.set_font("Helvetica", "B", 9)
            pdf.cell(35, 7, "Channel", border=1)
            pdf.cell(20, 7, "Priority", border=1, align="C")
            pdf.cell(25, 7, "Budget/mo", border=1, align="C")
            pdf.cell(20, 7, "CAC", border=1, align="C")
            pdf.cell(0, 7, "Key Tactics", border=1, new_x="LMARGIN", new_y="NEXT")
            pdf.set_font("Helvetica", "", 8)
            for ch in channels:
                tactics = ", ".join(ch.get("tactics", []))[:50]
                pdf.cell(35, 7, s(ch.get("name", "")[:20]), border=1)
                pdf.cell(20, 7, s(ch.get("priority", "")), border=1, align="C")
                pdf.cell(25, 7, s(_fmt_usd(ch.get("budget_monthly_usd", 0))), border=1, align="C")
                pdf.cell(20, 7, s(f"${ch.get('expected_cac', 0)}"), border=1, align="C")
                pdf.cell(0, 7, s(tactics), border=1, new_x="LMARGIN", new_y="NEXT")
            pdf.ln(5)

        # Launch strategy
        launch = ms.get("launch_strategy", {})
        if launch:
            sub_header("Launch Playbook")
            for phase, label in [("pre_launch", "Pre-Launch"), ("launch_week", "Launch Week"), ("post_launch", "Post-Launch (30-90 days)")]:
                items = launch.get(phase, [])
                if items:
                    pdf.set_font("Helvetica", "B", 10)
                    pdf.cell(0, 7, s(label), new_x="LMARGIN", new_y="NEXT")
                    for item in items:
                        bullet(item)
                    pdf.ln(2)

    # --- Footer on every page ---
    for page_num in range(1, pdf.page + 1):
        pdf.page = page_num
        pdf.set_y(-15)
        pdf.set_font("Helvetica", "I", 8)
        pdf.cell(0, 8, s(f"Generated by Ide/AI  |  {project_name}  |  Page {page_num}"), align="C")

    return pdf.output()


