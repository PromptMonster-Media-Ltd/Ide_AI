def generate_docx(analysis: MarketAnalysis, project_name: str) -> bytes:
    """Generate a Word document market analysis report using python-docx."""
    from docx import Document
    from docx.shared import Inches, Pt
    from docx.enum.text import WD_ALIGN_PARAGRAPH

    doc = Document()

    # Title
    title = doc.add_heading(project_name, level=0)
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    subtitle = doc.add_paragraph("Market Analysis Report")
    subtitle.alignment = WD_ALIGN_PARAGRAPH.CENTER
    subtitle.style = doc.styles["Subtitle"]

    date_p = doc.add_paragraph(f"Generated: {datetime.utcnow().strftime('%B %d, %Y')}")
    date_p.alignment = WD_ALIGN_PARAGRAPH.CENTER

    doc.add_page_break()

    # --- Executive Summary ---
    doc.add_heading("Executive Summary", level=1)
    summaries = []
    if analysis.target_market and isinstance(analysis.target_market, dict):
        summaries.append(analysis.target_market.get("summary", ""))
    if analysis.competitive_landscape and isinstance(analysis.competitive_landscape, dict):
        summaries.append(analysis.competitive_landscape.get("summary", ""))
    if analysis.marketing_strategies and isinstance(analysis.marketing_strategies, dict):
        summaries.append(analysis.marketing_strategies.get("summary", ""))
    doc.add_paragraph(" ".join(filter(None, summaries)) or "No executive summary data available.")

    # Key metrics
    if analysis.market_metrics and isinstance(analysis.market_metrics, dict):
        metrics = analysis.market_metrics
        doc.add_heading("Key Market Metrics", level=2)
        table = doc.add_table(rows=1, cols=3)
        table.style = "Light Grid Accent 1"
        for i, (label, key) in enumerate([("TAM", "tam"), ("SAM", "sam"), ("SOM", "som")]):
            cell = table.rows[0].cells[i]
            val = _fmt_usd(metrics.get(key, {}).get("value_usd", 0))
            cell.text = f"{label}: {val}"

    # --- Target Market ---
    if analysis.target_market and isinstance(analysis.target_market, dict):
        tm = analysis.target_market
        doc.add_heading("Target Market Assessment", level=1)
        doc.add_paragraph(_safe(tm.get("summary")))

        # Demographics
        demographics = tm.get("demographics", {})
        if demographics:
            doc.add_heading("Demographics", level=2)
            table = doc.add_table(rows=len(demographics) + 1, cols=2)
            table.style = "Light Grid Accent 1"
            table.rows[0].cells[0].text = "Attribute"
            table.rows[0].cells[1].text = "Value"
            for i, (key, val) in enumerate(demographics.items(), 1):
                table.rows[i].cells[0].text = key.replace("_", " ").title()
                table.rows[i].cells[1].text = str(val)

        # Market segments
        segments = tm.get("market_segments", [])
        if segments:
            doc.add_heading("Market Segments", level=2)
            table = doc.add_table(rows=len(segments) + 1, cols=4)
            table.style = "Light Grid Accent 1"
            for i, h in enumerate(["Segment", "Size %", "Priority", "Description"]):
                table.rows[0].cells[i].text = h
            for i, seg in enumerate(segments, 1):
                table.rows[i].cells[0].text = seg.get("name", "")
                table.rows[i].cells[1].text = f"{seg.get('size_pct', 0)}%"
                table.rows[i].cells[2].text = seg.get("priority", "")
                table.rows[i].cells[3].text = seg.get("description", "")

        # User personas
        personas = tm.get("user_personas", [])
        if personas:
            doc.add_heading("User Personas", level=2)
            for persona in personas:
                p = doc.add_paragraph(style="List Bullet")
                run = p.add_run(f"{persona.get('name', 'Unnamed')} ({persona.get('role', '')})")
                run.bold = True
                p.add_run(f"\nGoal: {persona.get('goal', 'N/A')}")
                p.add_run(f"\nFrustration: {persona.get('frustration', 'N/A')}")

    # --- Competitive Landscape ---
    if analysis.competitive_landscape and isinstance(analysis.competitive_landscape, dict):
        cl = analysis.competitive_landscape
        doc.add_heading("Competitive Landscape", level=1)
        doc.add_paragraph(_safe(cl.get("summary")))

        direct = cl.get("direct_competitors", [])
        if direct:
            doc.add_heading("Direct Competitors", level=2)
            table = doc.add_table(rows=len(direct) + 1, cols=5)
            table.style = "Light Grid Accent 1"
            for i, h in enumerate(["Competitor", "Market Share", "Threat Level", "Strengths", "Weaknesses"]):
                table.rows[0].cells[i].text = h
            for i, comp in enumerate(direct, 1):
                table.rows[i].cells[0].text = comp.get("name", "")
                table.rows[i].cells[1].text = f"{comp.get('market_share_pct', 'N/A')}%"
                table.rows[i].cells[2].text = comp.get("threat_level", "")
                table.rows[i].cells[3].text = ", ".join(comp.get("strengths", []))
                table.rows[i].cells[4].text = ", ".join(comp.get("weaknesses", []))

        advantages = cl.get("competitive_advantages", [])
        if advantages:
            doc.add_heading("Your Competitive Advantages", level=2)
            for adv in advantages:
                doc.add_paragraph(adv, style="List Bullet")

        gaps = cl.get("market_gaps", [])
        if gaps:
            doc.add_heading("Market Gaps & Opportunities", level=2)
            for gap in gaps:
                doc.add_paragraph(gap, style="List Bullet")

    # --- Market Metrics ---
    if analysis.market_metrics and isinstance(analysis.market_metrics, dict):
        metrics = analysis.market_metrics
        doc.add_heading("Market Metrics & TAM/SAM/SOM", level=1)

        for key_name, label in [("tam", "TAM (Total Addressable Market)"), ("sam", "SAM (Serviceable Addressable Market)"), ("som", "SOM (Serviceable Obtainable Market)")]:
            metric = metrics.get(key_name, {})
            if metric:
                doc.add_heading(label, level=2)
                p = doc.add_paragraph()
                run = p.add_run(_fmt_usd(metric.get("value_usd", 0)))
                run.bold = True
                run.font.size = Pt(14)
                doc.add_paragraph(_safe(metric.get("description")))
                if metric.get("methodology"):
                    p = doc.add_paragraph()
                    run = p.add_run(f"Methodology: {metric['methodology']}")
                    run.italic = True

        trends = metrics.get("industry_trends", [])
        if trends:
            doc.add_heading("Industry Trends", level=2)
            for trend in trends:
                direction = trend.get("direction", "neutral")
                indicator = "[+]" if direction == "positive" else "[-]" if direction == "negative" else "[~]"
                doc.add_paragraph(f"{indicator} {trend.get('trend', '')} -- {trend.get('impact', '')}", style="List Bullet")

    # --- Revenue Projections ---
    if analysis.revenue_projections and isinstance(analysis.revenue_projections, dict):
        rev = analysis.revenue_projections
        doc.add_heading("Revenue Projections", level=1)

        p = doc.add_paragraph()
        run = p.add_run(f"Recommended Pricing Model: {_safe(rev.get('pricing_model'))}")
        run.bold = True
        if rev.get("break_even_months"):
            doc.add_paragraph(f"Estimated Break-Even: {rev['break_even_months']} months")

        # Pricing tiers
        tiers = rev.get("pricing_tiers", [])
        if tiers:
            doc.add_heading("Pricing Tiers", level=2)
            table = doc.add_table(rows=len(tiers) + 1, cols=4)
            table.style = "Light Grid Accent 1"
            for i, h in enumerate(["Tier", "Price/mo", "Target Segment", "Features"]):
                table.rows[0].cells[i].text = h
            for i, tier in enumerate(tiers, 1):
                price = "Free" if tier.get("price_monthly", 0) == 0 else f"${tier.get('price_monthly', 0)}/mo"
                table.rows[i].cells[0].text = tier.get("name", "")
                table.rows[i].cells[1].text = price
                table.rows[i].cells[2].text = tier.get("target_segment", "")
                table.rows[i].cells[3].text = ", ".join(tier.get("features", []))

        # Scenarios
        scenarios = rev.get("scenarios", {})
        if scenarios:
            doc.add_heading("3-Year Revenue Scenarios", level=2)
            table = doc.add_table(rows=4, cols=7)
            table.style = "Light Grid Accent 1"
            headers = ["Scenario", "Year 1", "Year 2", "Year 3", "Yr1 MRR", "Yr3 Users", "Yr3 Paying"]
            for i, h in enumerate(headers):
                table.rows[0].cells[i].text = h
            for row_idx, (scenario_key, label) in enumerate([("best_case", "Best"), ("likely_case", "Likely"), ("worst_case", "Worst")], 1):
                sc = scenarios.get(scenario_key, {})
                y1 = sc.get("year_1", {})
                y2 = sc.get("year_2", {})
                y3 = sc.get("year_3", {})
                table.rows[row_idx].cells[0].text = label
                table.rows[row_idx].cells[1].text = _fmt_usd(y1.get("revenue", 0))
                table.rows[row_idx].cells[2].text = _fmt_usd(y2.get("revenue", 0))
                table.rows[row_idx].cells[3].text = _fmt_usd(y3.get("revenue", 0))
                table.rows[row_idx].cells[4].text = _fmt_usd(y1.get("mrr", 0))
                table.rows[row_idx].cells[5].text = _fmt_number(y3.get("users", 0))
                table.rows[row_idx].cells[6].text = _fmt_number(y3.get("paying_users", 0))

    # --- Marketing Strategy ---
    if analysis.marketing_strategies and isinstance(analysis.marketing_strategies, dict):
        ms = analysis.marketing_strategies
        doc.add_heading("Marketing Strategy", level=1)
        doc.add_paragraph(_safe(ms.get("summary")))

        budget = ms.get("budget_summary", {})
        if budget:
            doc.add_heading("Budget Overview", level=2)
            monthly_min = _fmt_usd(budget.get("monthly_min_usd", 0))
            monthly_max = _fmt_usd(budget.get("monthly_max_usd", 0))
            doc.add_paragraph(f"Monthly Range: {monthly_min} -- {monthly_max}")
            allocation = budget.get("allocation", [])
            for alloc in allocation:
                doc.add_paragraph(f"{alloc.get('category', '')}: {alloc.get('pct', 0)}%", style="List Bullet")

        channels = ms.get("channels", [])
        if channels:
            doc.add_heading("Marketing Channels", level=2)
            table = doc.add_table(rows=len(channels) + 1, cols=5)
            table.style = "Light Grid Accent 1"
            for i, h in enumerate(["Channel", "Priority", "Budget/mo", "CAC", "Tactics"]):
                table.rows[0].cells[i].text = h
            for i, ch in enumerate(channels, 1):
                table.rows[i].cells[0].text = ch.get("name", "")
                table.rows[i].cells[1].text = ch.get("priority", "")
                table.rows[i].cells[2].text = _fmt_usd(ch.get("budget_monthly_usd", 0))
                table.rows[i].cells[3].text = f"${ch.get('expected_cac', 0)}"
                table.rows[i].cells[4].text = ", ".join(ch.get("tactics", []))

        launch = ms.get("launch_strategy", {})
        if launch:
            doc.add_heading("Launch Playbook", level=2)
            for phase, label in [("pre_launch", "Pre-Launch"), ("launch_week", "Launch Week"), ("post_launch", "Post-Launch (30-90 days)")]:
                items = launch.get(phase, [])
                if items:
                    doc.add_heading(label, level=3)
                    for item in items:
                        doc.add_paragraph(item, style="List Bullet")

    # Footer
    footer = doc.add_paragraph()
    footer.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = footer.add_run("Generated by Ide/AI")
    run.italic = True

    buf = io.BytesIO()
    doc.save(buf)
    return buf.getvalue()


