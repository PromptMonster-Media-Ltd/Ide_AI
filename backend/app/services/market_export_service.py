def _fmt_usd(n: int | float | None) -> str:
    """Format a USD value with appropriate suffix."""
    if n is None:
        return "$0"
    if n >= 1_000_000_000:
        return f"${n / 1_000_000_000:.1f}B"
    if n >= 1_000_000:
        return f"${n / 1_000_000:.1f}M"
    if n >= 1_000:
        return f"${n / 1_000:.0f}K"
    return f"${n:,.0f}"


def _fmt_number(n: int | float | None) -> str:
    """Format a number with appropriate suffix."""
    if n is None:
        return "0"
    if n >= 1_000_000:
        return f"{n / 1_000_000:.1f}M"
    if n >= 1_000:
        return f"{n / 1_000:.1f}K"
    return f"{n:,}"


def _safe(val, default="N/A"):
    """Return val if truthy, else default."""
    return val if val else default


